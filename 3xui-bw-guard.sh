}

need_root() { [[ "${EUID:-$(id -u)}" -eq 0 ]] || { error "Run as root."; exit 1; }; }
cmd_exists(){ command -v "$1" >/dev/null 2>&1; }
trim(){ local s="$*"; s="${s#${s%%[![:space:]]*}}"; s="${s%${s##*[![:space:]]}}"; printf '%s' "$s"; }
is_yes(){ [[ "${1,,}" == "y" || "${1,,}" == "yes" || "${1,,}" == "1" || "${1,,}" == "true" ]]; }
ensure_dirs(){ mkdir -p "$APP_DIR" "$STATE_DIR"; }

prompt_default(){
  local question="$1" default="$2" answer=""
  if [[ -t 0 ]]; then
    if [[ -n "$default" ]]; then
      read -r -p "$question [$default]: " answer || true
    else
      read -r -p "$question: " answer || true
    fi
    answer="$(trim "$answer")"
    if [[ -z "$answer" ]]; then
      printf '%s' "$default"
    else
      printf '%s' "$answer"
    fi
  else
    printf '%s' "$default"
  fi
}

confirm(){
  local question="$1" default="${2:-y}" answer=""
  if [[ ! -t 0 ]]; then
    [[ "$default" == "y" ]]
    return
  fi
  if [[ "$default" == "y" ]]; then
    read -r -p "$question [Y/n]: " answer || true
    answer="$(trim "$answer")"
    [[ -z "$answer" ]] && return 0
    is_yes "$answer"
  else
    read -r -p "$question [y/N]: " answer || true
    answer="$(trim "$answer")"
    [[ -z "$answer" ]] && return 1
    is_yes "$answer"
  fi
}

