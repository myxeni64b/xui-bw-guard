show_config(){
  load_config
  banner
  header "Current configuration"
  sed -n '1,200p' "$CONF_FILE"
}

show_status(){
  load_config || true
  banner
  header "Service status"
  systemctl --no-pager --full status "$APP_NAME" || true
  echo
  header "Shaping classes (egress)"
  iface="$(detect_iface 2>/dev/null || true)"
  [[ -n "$iface" ]] && tc -s class show dev "$iface" 2>/dev/null || true
  echo
  header "Shaping classes (ingress/ifb)"
  [[ -n "${INGRESS_IFB:-}" ]] && tc -s class show dev "${INGRESS_IFB:-ifb3xui0}" 2>/dev/null || true
}

doctor(){
  banner
  header "Environment checks"
  local missing=0 c
  for c in ip tc nft conntrack jq ethtool modprobe systemctl; do
    if cmd_exists "$c"; then
      success "found: $c"
    else
      warn "missing: $c"
      missing=1
    fi
  done
  if [[ -f "$CONF_FILE" ]]; then
    success "config present: $CONF_FILE"
    load_config || true
    info "configured interface: ${IFACE:-auto}"
    info "configured xray path : ${XRAY_CONFIG_PATH:-<empty>}"
  else
    warn "config missing: $CONF_FILE"
  fi
  if cmd_exists modprobe; then
    for m in ifb sch_htb sch_fq_codel cls_flower act_mirred; do
      if modprobe -n "$m" >/dev/null 2>&1; then
        success "module ok: $m"
      else
        warn "module may be unavailable: $m"
      fi
    done
  fi
  if [[ "$missing" -eq 0 ]]; then
    success "Environment looks ready."
  else
    warn "Some required commands are missing."
  fi
}

wizard(){
  need_root
  banner
  header "Interactive setup wizard"
  mkdir -p "$APP_DIR"
  if [[ -f "$CONF_FILE" ]]; then
    load_config || true
    info "Loaded existing config: $CONF_FILE"
  else
    IFACE="auto"
    INGRESS_IFB="ifb3xui0"
    XRAY_CONFIG_PATH=""
    MANAGED_PORTS=""
    EXCLUDE_PORTS=""
    EXEMPT_IPS="127.0.0.1,::1"
    LIMIT_DOWN="5mbit"
    LIMIT_UP="1mbit"
    UPLINK_RATE="auto"
    INGRESS_RATE="auto"
    MAX_CONN_PER_IP="0"
    SMART_BURST="yes"
    BURST_INTERVAL_MS="120"
    SCAN_INTERVAL="5"
    FLOW_GRACE_SECONDS="30"
    LOG_LEVEL="info"
  fi

  IFACE="$(prompt_default 'Network interface (or auto)' "${IFACE:-auto}")"
  INGRESS_IFB="$(prompt_default 'IFB interface name' "${INGRESS_IFB:-ifb3xui0}")"
  XRAY_CONFIG_PATH="$(prompt_default '3x-ui/Xray config.json path (blank for auto-discovery)' "${XRAY_CONFIG_PATH:-}")"
  MANAGED_PORTS="$(prompt_default 'Managed inbound ports CSV (blank to auto-detect)' "${MANAGED_PORTS:-}")"
  EXCLUDE_PORTS="$(prompt_default 'Excluded ports CSV' "${EXCLUDE_PORTS:-}")"
  EXEMPT_IPS="$(prompt_default 'Exempt IPs CSV' "${EXEMPT_IPS:-127.0.0.1,::1}")"
  LIMIT_DOWN="$(prompt_default 'Per-client download limit' "${LIMIT_DOWN:-5mbit}")"
  LIMIT_UP="$(prompt_default 'Per-client upload limit' "${LIMIT_UP:-1mbit}")"
  UPLINK_RATE="$(prompt_default 'Real uplink capacity' "${UPLINK_RATE:-auto}")"
  INGRESS_RATE="$(prompt_default 'Real ingress capacity' "${INGRESS_RATE:-auto}")"
  MAX_CONN_PER_IP="$(prompt_default 'Max connections per IP (0 disables)' "${MAX_CONN_PER_IP:-0}")"
  SMART_BURST="$(prompt_default 'Smart burst (yes/no)' "${SMART_BURST:-yes}")"
  BURST_INTERVAL_MS="$(prompt_default 'Burst interval in milliseconds' "${BURST_INTERVAL_MS:-120}")"
  SCAN_INTERVAL="$(prompt_default 'Scan interval in seconds' "${SCAN_INTERVAL:-5}")"
  FLOW_GRACE_SECONDS="$(prompt_default 'Flow grace seconds' "${FLOW_GRACE_SECONDS:-30}")"
  LOG_LEVEL="$(prompt_default 'Log level (info/debug)' "${LOG_LEVEL:-info}")"

  save_config
  success "Configuration saved to $CONF_FILE"
  if confirm 'Run config test now?' y; then
    configtest || true
  fi
  if confirm 'Enable and start the service now?' y; then
    systemctl enable --now "$APP_NAME"
    success "Service enabled and started"
  fi
}

install_all(){
  need_root
  banner
  install_deps
  ensure_dirs
  copy_self
  write_default_config
  write_service
  success "Install complete."
  info "Config file : $CONF_FILE"
  info "Service     : $SERVICE_FILE"
  info "Binary      : $INSTALL_PATH"
  if [[ -t 0 ]] && confirm 'Launch setup wizard now?' y; then
    "$INSTALL_PATH" wizard
  else
    info "Next step: edit $CONF_FILE or run $INSTALL_PATH wizard"
  fi
}

uninstall_all(){
  need_root
  systemctl disable --now "$APP_NAME" 2>/dev/null || true
  cleanup_nft
  cleanup_qdisc
  rm -f "$SERVICE_FILE" "$INSTALL_PATH"
  rm -rf "$APP_DIR" "$STATE_DIR"
  systemctl daemon-reload
  success "Uninstalled $APP_NAME"
}

show_help(){
  banner
  cat <<USAGE
Usage: $0 <command>

Commands:
  install       Install dependencies, binary, config, and service
  uninstall     Remove service, config, runtime, and installed binary
  wizard        Interactive configuration wizard
  doctor        Check commands, modules, and config presence
  config        Show current configuration file
  configtest    Validate current configuration and detected ports
  apply         Apply base runtime config immediately
  enable        Enable systemd service
  disable       Disable systemd service
  start         Start systemd service
  stop          Stop systemd service
  restart       Restart systemd service
  status        Show service and tc class status
  run-daemon    Internal service entrypoint
  stop-runtime  Internal cleanup entrypoint
  version       Show script version
  help          Show this help
USAGE
}

case "${1:-help}" in
  install) install_all ;;
  uninstall) uninstall_all ;;
  wizard) wizard ;;
  doctor) doctor ;;
  config) show_config ;;
  enable) systemctl enable "$APP_NAME" ;;
  disable) systemctl disable "$APP_NAME" ;;
  start) systemctl start "$APP_NAME" ;;
  stop) systemctl stop "$APP_NAME" ;;
  restart) systemctl restart "$APP_NAME" ;;
  status) show_status ;;
  configtest) configtest ;;
  apply) need_root; load_config; resolve_rates; ensure_port_array; setup_qdisc; build_nft; success "Applied base runtime config. Restart service to fully reconcile clients." ;;
  run-daemon) run_daemon ;;
  stop-runtime) stop_runtime ;;
  version) printf '%s\n' "$APP_VERSION" ;;
  help|-h|--help) show_help ;;
  *) error "Unknown command: ${1}"; show_help; exit 1 ;;
esac
